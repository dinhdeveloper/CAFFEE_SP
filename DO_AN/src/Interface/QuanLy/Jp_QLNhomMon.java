    public void getData() {
        DefaultTableModel model = new DefaultTableModel();
        model.addColumn("Mã Loại");
        model.addColumn("Tên Loại");
        model.addColumn("Màu Sắc");
        NhomMonDAO nvDao = new NhomMonDAO();
        int soloai = 0;
        for (Loai nv : NhomMonDAO.getData()) {
            soloai++;
            model.addRow(new Object[]{nv.getMaLoai(), nv.getTenLoai(), nv.getMauSac()});
        }
        jTable1.setModel(model);
        jLTongSoLoai.setText(String.valueOf(soloai) + " Loại");
    }

