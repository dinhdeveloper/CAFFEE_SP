    private void btnThemLoaiActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThemLoaiActionPerformed
        if (txtMaLoai.getText().equals("") || txtTenLoai.getText().equals("") || txtMauSac.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Không được rỗng");
        } else {
            try (Connection conn = DriverManager.getConnection(ConnectSQL.ConnectSQL());
                    PreparedStatement ps = conn.prepareCall("{call insertNhomMon(?,?,?)}")) {

                ps.setString(1, txtMaLoai.getText());
                ps.setString(2, txtTenLoai.getText());
                ps.setString(3, txtMauSac.getText());
                ps.executeUpdate();
                JOptionPane.showMessageDialog(null, "Thêm Loại Thành Công");
            } catch (Exception ex) {
                ex.printStackTrace();
            }
        }
        getData();
    }//GEN-LAST:event_btnThemLoaiActionPerformed

    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        try {
            int i = jTable1.getSelectedRow();
            TableModel model = jTable1.getModel();
            txtMaLoai.setEnabled(false);
            txtMaLoai.setText(model.getValueAt(i, 0).toString());
            txtTenLoai.setText(model.getValueAt(i, 1).toString());
            txtMauSac.setText(model.getValueAt(i, 2).toString());
        } catch (Exception ex) {
            ex.printStackTrace();
        }
    }//GEN-LAST:event_jTable1MouseClicked

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        if (txtTenLoai.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Vui lòng chọn loại muốn cập nhật");
        } else {
            try (Connection conn = DriverManager.getConnection(ConnectSQL.ConnectSQL());
                    PreparedStatement ps = conn.prepareCall("{call updateNhomMon(?,?,?)}")) {
                ps.setString(1, txtTenLoai.getText());
                ps.setString(2, txtMauSac.getText());
                ps.setInt(3, Integer.parseInt(txtMaLoai.getText()));
                ps.executeUpdate();
                JOptionPane.showMessageDialog(null, "Cập Nhật Loại Thành Công");
            } catch (Exception ex) {
                JOptionPane.showMessageDialog(null, ex.getMessage(), "ERROR",
                        JOptionPane.ERROR_MESSAGE);
            }
            getData();
            setTestfield();
        }
    }//GEN-LAST:event_jButton2ActionPerformed

    private void btnXoaLoaiActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnXoaLoaiActionPerformed
        int kq = JOptionPane.showConfirmDialog(null, "Bạn muốn xóa loại này không?", "Chú ý", JOptionPane.YES_NO_OPTION);
        if (kq == 0) {
            if (txtTenLoai.getText().equals("")) {
                JOptionPane.showMessageDialog(null, "Vui lòng chọn loại muốn xóa");
            } else {
                try (Connection conn = DriverManager.getConnection(ConnectSQL.ConnectSQL());
                        PreparedStatement ps = conn.prepareCall("{call deleteNhomMon(?)}")) {
                    ps.setInt(1, Integer.parseInt(txtMaLoai.getText()));
                    ps.executeUpdate();
                    JOptionPane.showMessageDialog(null, "Xóa Loại Thành Công");

                } catch (SQLException ex) {
                    JOptionPane.showMessageDialog(null, ex.getMessage(), "ERROR",
                            JOptionPane.ERROR_MESSAGE);
                }
            }
            getData();
            setTestfield();
        }
    }//GEN-LAST:event_btnXoaLoaiActionPerformed

    private void txtTimLoaiKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtTimLoaiKeyReleased
        DefaultTableModel model = new DefaultTableModel();
        model.addColumn("Mã Loại");
        model.addColumn("Tên Loại");
        model.addColumn("Màu Sắc");
        String str = txtTimLoai.getText();
        jTable1.setAutoCreateRowSorter(true);
        DefaultRowSorter<?, ?> sorter = (DefaultRowSorter<?, ?>) jTable1.getRowSorter();
        sorter.setRowFilter(RowFilter.regexFilter(str));
        sorter.setSortKeys(null);
        jTable1.setRowSorter((RowSorter<? extends TableModel>) sorter);
    }//GEN-LAST:event_txtTimLoaiKeyReleased

