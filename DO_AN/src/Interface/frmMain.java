    public void reloadPanel(int i) {
        jPanel2.removeAll();
        switch (i) {
            case 1:
                if (home == null) {
                    home = new JpHome();
                }
                jPanel2.add(home);
                home.setSize(1300, 800);
                //btnBanHang.setPressedIcon(new ImageIcon("down.png"));
                jPanel2.revalidate();
                this.repaint();
                break;
            case 2:
                if (banhang == null) {
                    banhang = new jpBanHang();
                }
                jPanel2.add(banhang);
                banhang.setSize(1300, 800);
                jPanel2.revalidate();
                this.repaint();
                break;
            case 3:
                if (ql == null) {
                    ql = new JpQuanLy();
                }
                jPanel2.add(ql);
                ql.setSize(1300, 800);
                jPanel2.revalidate();
                this.repaint();
                break;
            case 4:
                if (tke == null) {
                    tke = new JpThongKe();
                }
                jPanel2.add(tke);
                tke.setSize(1300, 800);
                jPanel2.revalidate();
                this.repaint();
                break;
            case 5:
                if (kho == null) {
                    kho = new JpKho();
                }
                jPanel2.add(kho);
                kho.setSize(1300, 800);
                jPanel2.revalidate();
                this.repaint();
                break;
            case 6:
                if (Set == null) {
                    Set = new JpSetting();
                }
                jPanel2.add(Set);
                Set.setSize(1300, 800);
                jPanel2.revalidate();
                this.repaint();
                break;
            default:
                break;
        }
    }

